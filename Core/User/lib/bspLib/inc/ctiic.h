#define CT_SDA_OUT() {GPIOB->MODER&=~(3<<(2*9));GPIOB->MODER|=1<<2*9;} 	//PB9输出模式
//IO操作函数	 
#define CT_IIC_SCL    PBout(8) 	//SCL
#define CT_IIC_SDA    PBout(9) 	//SDA	 
#define CT_READ_SDA   PBin(9)  	//输入SDA 
 

#define GPIO_A 				0
#define GPIO_B 				1
#define GPIO_C				2
#define GPIO_D 				3
#define GPIO_E 				4
#define GPIO_F 				5
#define GPIO_G 				6 
#define GPIO_H 				7 
#define GPIO_I 				8 

#define FTIR   				1  		//下降沿触发
